char	*ft_strrchr(const char *str, int c)
{
	int			i;
	const char	*tmp;

	i = 0;
	while (str[i])
	{
		if ((int)str[i] == c)
			tmp = &str[i];
		i++;
	}
	return ((char *)tmp);
}
