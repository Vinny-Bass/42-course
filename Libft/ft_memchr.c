#include <stdlib.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t						i;
	const unsigned char			*str;
	unsigned char				uc;

	i = 0;
	str = (const unsigned char *)s;
	uc = (unsigned char)c;
	while (i < n)
	{
		if (str[i] == uc)
			return ((void *)&str[i]);
		i++;
	}
	return (NULL);
}
